#include "bsp_Init.h"
/***********************初始化函数***********************/

void Init(void)
{
            /*          串口初始化            */
            TMOD=0x20;        //使用定时工作方式2
/******************波特率为9600***********************/      
            TL1 = 0xFD;       //设定定时初值
            TH1 = 0xFD;       //设定定时器重装值
            PCON=0x00;        //波特率不倍速

            SCON=0x50;
      
            EA=1;
            ES=1;
            TR1=1;            //打开定时器中断
}